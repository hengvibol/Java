package Loop_Of_Java;
public class For_loop_01 {
    public static void main(String[] args) {
        int i;
        System.out.println("For loop statement 1");
        for(i=1;i<=10;i++){
            System.out.print(" "+i);
        }
        System.out.println("");
        System.out.println("For loop statement 2");
        for(i=10;i>=1;i--){
            System.out.print(" "+i);
        }
    }
}
