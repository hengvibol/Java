package Loop_Of_Java;
public class For_Each_03 {
    public static void main(String[] args) {
        int a[]={10,20,30,50};
        for(int temp : a){
            System.out.print(temp);
        }
        String name[]={"vibol","Meng","Ory"};
        for(String boo : name){
            System.out.println(boo);
    }
}
}
